# Comprehensive test of all operators in PyAOT

print(10)
print(3)

# Modulo operator (%)
a = 10 % 3
print(a)

# Floor division operator (//)
b = 10 // 3
print(b)

# Exponentiation operator (**)
c = 2 ** 3
print(c)

# Bitwise AND operator (&)
d = 12 & 10
print(d)

# Bitwise OR operator (|)
e = 12 | 10
print(e)

# Bitwise XOR operator (^)
f = 12 ^ 10
print(f)

# Combined test with multiple operators
g = 15 % 4
h = 15 // 4
i = 2 ** 4
print(g)
print(h)
print(i)

# Bitwise operations test
j = 7 & 3
k = 7 | 3
m = 7 ^ 3
print(j)
print(k)
print(m)
