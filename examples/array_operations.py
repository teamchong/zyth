# Array operations (constant list)
nums = [1, 2, 3, 4, 5]

# Subscript
print(nums[0])    # 1
print(nums[2])    # 3
print(nums[-1])   # 5

# Iteration
for n in nums:
    print(n)

# len()
print(len(nums))  # 5

# ArrayList operations (dynamic list)
dynamic = []
dynamic.append(10)
dynamic.append(20)

print(dynamic[0])  # 10
print(len(dynamic))  # 2

for d in dynamic:
    print(d)
