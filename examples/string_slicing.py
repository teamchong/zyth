# Test string slicing
message = "Hello, World!"

# Basic slicing
part = message[0:5]  # "Hello"
print("First part:")
print(part)

# Slice from start
greeting = message[:5]  # "Hello"
print("Greeting:")
print(greeting)

# Slice to end
ending = message[7:]  # "World!"
print("Ending:")
print(ending)

# Middle slice
middle = message[7:12]  # "World"
print("Middle:")
print(middle)
