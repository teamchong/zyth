# range() with different argument counts

# range(n) - 0 to n-1
print("range(5):")
for i in range(5):
    print(i)

# range(start, end)
print("range(2, 8):")
for i in range(2, 8):
    print(i)

# range(start, end, step)
print("range(0, 10, 2):")
for i in range(0, 10, 2):
    print(i)

print("range(1, 20, 3):")
for i in range(1, 20, 3):
    print(i)
